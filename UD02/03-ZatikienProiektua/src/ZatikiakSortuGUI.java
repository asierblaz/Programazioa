    private void sortuButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sortuButtonActionPerformed
        // TODO add your handling code here:
        String zenbat = (String) zenbatBox.getSelectedItem();

        int input = showConfirmDialog(null, zenbat + " Zatiki sortu nahi dituzu?");

        if (input == 0) {
            zatikiak = new Zatikia[Integer.parseInt(zenbat)];
            Zatikia.zatikiakRandom(zatikiak);

            input = showConfirmDialog(null, "Zatikiak simplifikatu nahi dituzu?");
            if (input == 0) {
                for (int i = 0; i < zatikiak.length; i++) {
                    zatikiak[i].sinplifikatu();
                }
            } else {
                buttonSimplifikatu.setVisible(true);
            }
            labelzatikiak.setText(Arrays.toString(zatikiak));
            labelsortu.setVisible(true);
            labelzatikiak.setVisible(true);

        } else {

            JOptionPane.showMessageDialog(null, "Ez dituzu zatikirik sortu ðŸ˜’ðŸ˜’", "Error", JOptionPane.WARNING_MESSAGE);
        }

        Ordenatu.setVisible(true);
    }//GEN-LAST:event_sortuButtonActionPerformed

