    private void comprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comprobarActionPerformed
        // TODO add your handling code here:
         labelOkerra.setVisible(false);
         sadlabel.setVisible(false);
        labelzuzena.setVisible(false);
        oklabel.setVisible(false);
        rdbutton1.setSelected(false);
        rdbutton2.setSelected(false);
        rdbutton3.setSelected(false);
        if (rdbutton1.isSelected()) {

            if (handiena() == zatikiak[0].hamartarBaliokidea()) {

                labelzuzena.setVisible(true);
                oklabel.setVisible(true);

            } else {
                labelOkerra.setVisible(true);
                sadlabel.setVisible(true);
            }

        }
        if (rdbutton2.isSelected()) {

            if (handiena() == zatikiak[1].hamartarBaliokidea()) {

                labelzuzena.setVisible(true);
                oklabel.setVisible(true);

            } else {
                labelOkerra.setVisible(true);
                sadlabel.setVisible(true);
            }

        }
        if (rdbutton3.isSelected()) {

            if (handiena() == zatikiak[2].hamartarBaliokidea()) {

                labelzuzena.setVisible(true);
                oklabel.setVisible(true);

            } else {
                labelOkerra.setVisible(true);
                sadlabel.setVisible(true);
            }

        }

    }//GEN-LAST:event_comprobarActionPerformed

