    private void sortu() {
        Zatikia.zatikiakRandom(zatikiak);

        zenbBat.setText(String.valueOf(zatikiak[0].getZenbakitzailea()));
        zenbBi.setText(String.valueOf(zatikiak[1].getZenbakitzailea()));
        zenbHiru.setText(String.valueOf(zatikiak[2].getZenbakitzailea()));
        izenBat.setText(String.valueOf(zatikiak[0].getIzendatzailea()));
        izenBi.setText(String.valueOf(zatikiak[1].getIzendatzailea()));
        izenHiru.setText(String.valueOf(zatikiak[2].getIzendatzailea()));
    }

    private double handiena() {
        double max = 0;
        for (int i = 0; i < zatikiak.length; i++) {
            if (zatikiak[i].hamartarBaliokidea() >= max) {
                max = zatikiak[i].hamartarBaliokidea();
            }
        }
        return max;

    }

