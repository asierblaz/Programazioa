public class SumProduct3Numbers {

 public static void main(String[] args) {
      int number1 = 98; 
      int number2 = 5;  
      int number3=77;
      int sum, product;  // Declare 5 int variables to hold results
   
      // Perform arithmetic Operations
      sum = number1 + number2 + number3;
      product = number1 * number2 * number3;
  
      // Print results
      System.out.println("The sum of "+number1+", "+ number2+" and "+number3+" is : "+ sum);  // Print description
      System.out.println("The product of "+number1+", "+ number2+" and "+number3+" is : "+ product);  // Print description
      

   
     
   }
}
